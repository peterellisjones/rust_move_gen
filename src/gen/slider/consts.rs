use bb::BB;
use square::Square;

#[inline]
pub fn squares_between(a: Square, b: Square) -> BB {
  unsafe {
    *SQUARES_BETWEEN
      .get_unchecked(a.to_usize())
      .get_unchecked(b.to_usize())
  }
}

#[inline]
pub fn lines_along(a: Square, b: Square) -> BB {
  unsafe {
    *LINES_ALONG
      .get_unchecked(a.to_usize())
      .get_unchecked(b.to_usize())
  }
}

